package ViewLogin;

public class CreditCard {
	String username;
	String creditNumField;
	String firstnameField;
	String lastnameField;
	int monthField;
	int yearField;
	String billingAddress1Field;
	String billingAddress2Field;
	String cityField;
	String stateField;
	int zipField;
	String phoneField;	
	String cardType;	

	public CreditCard() {

	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getCreditNumField() {
		return creditNumField;
	}

	public void setCreditNumField(String creditNumField) {
		this.creditNumField = creditNumField;
	}

	public String getFirstnameField() {
		return firstnameField;
	}

	public void setFirstnameField(String firstnameField) {
		this.firstnameField = firstnameField;
	}

	public String getLastnameField() {
		return lastnameField;
	}

	public void setLastnameField(String lastnameField) {
		this.lastnameField = lastnameField;
	}

	public int getMonthField() {
		return monthField;
	}

	public void setMonthField(int monthField) {
		this.monthField = monthField;
	}

	public int getYearField() {
		return yearField;
	}

	public void setYearField(int yearField) {
		this.yearField = yearField;
	}

	public String getBillingAddress1Field() {
		return billingAddress1Field;
	}

	public void setBillingAddress1Field(String billingAddress1Field) {
		this.billingAddress1Field = billingAddress1Field;
	}

	public String getBillingAddress2Field() {
		return billingAddress2Field;
	}

	public void setBillingAddress2Field(String billingAddress2Field) {
		this.billingAddress2Field = billingAddress2Field;
	}

	public String getCityField() {
		return cityField;
	}

	public void setCityField(String cityField) {
		this.cityField = cityField;
	}

	public String getStateField() {
		return stateField;
	}

	public void setStateField(String stateField) {
		this.stateField = stateField;
	}

	public int getZipField() {
		return zipField;
	}

	public void setZipField(int zipField) {
		this.zipField = zipField;
	}

	public String getPhoneField() {
		return phoneField;
	}

	public void setPhoneField(String phoneField) {
		this.phoneField = phoneField;
	}

	public String getCardType() {
		return cardType;
	}

	public void setCardType(String cardType) {
		this.cardType = cardType;
	}

}
